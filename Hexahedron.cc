/** draw many Hexahedrons
 *
 *  \param p0       first index of point to draw
 *  \param p1       last index of point to draw
 *  \param colour   colour to use if no data
 *  \param data     data associated with nodes (NULL for no data display)
 *  \param stride   draw every n'th point
 */
void Hexahedron::draw( int p0, int p1, GLfloat *colour, Colourscale* cs,
                       DATA_TYPE* data, int stride, dataOpac* dataopac )
{
  if ( data != NULL ) {
    for ( int p=4*p0; p<=4*p1+3; p+= stride*4 ) {
      int an, bn, cn, dn, en, fn, gn, hn;

      if (!_pt->vis(_node[p])||!_pt->vis(_node[p+1])||!_pt->vis(_node[p+2])||
          !_pt->vis(_node[p+3])||!_pt->vis(_node[p+4])||!_pt->vis(_node[p+5])||
          !_pt->vis(_node[p+6])||!_pt->vis(_node[p+7] ))
        continue;

      const GLfloat* a =  _pt->pt(an=_node[p]);
      const GLfloat* b =  _pt->pt(dn=_node[p+1]);
      const GLfloat* c =  _pt->pt(cn=_node[p+2]);
      const GLfloat* d =  _pt->pt(dn=_node[p+3]);
      const GLfloat* e =  _pt->pt(en=_node[p+2]);
      const GLfloat* f =  _pt->pt(fn=_node[p+5]);
      const GLfloat* g =  _pt->pt(gn=_node[p+6]);
      const GLfloat* h =  _pt->pt(hn=_node[p+7]);

      GLfloat* ca = cs->colorvec( data[an] );
      GLfloat* cb = cs->colorvec( data[bn] );
      GLfloat* cc = cs->colorvec( data[cn] );
      GLfloat* cd = cs->colorvec( data[dn] );
      GLfloat* ce = cs->colorvec( data[en] );
      GLfloat* cf = cs->colorvec( data[fn] );
      GLfloat* cg = cs->colorvec( data[gn] );
      GLfloat* ch = cs->colorvec( data[hn] );

      glBegin(GL_QUAD_STRIP);
      glColor3fv( ca );
      glVertex3fv( a );
      glColor3fv( cb );
      glVertex3fv( b );
      glColor3fv( cd );
      glVertex3fv( d );
      glColor3fv( cc );
      glVertex3fv( c );
      glColor3fv( cf );
      glVertex3fv( f );
      glColor3fv( cg );
      glVertex3fv( g );
      glColor3fv( ce );
      glVertex3fv( e );
      glColor3fv( ch );
      glVertex3fv( h );
      glColor3fv( ca );
      glVertex3fv( a );
      glColor3fv( cb );
      glVertex3fv( b );
      glEnd();
      glBegin(GL_QUADS);
      glColor3fv( ca );
      glVertex3fv( a );
      glColor3fv( cb );
      glVertex3fv( b );
      glColor3fv( cc );
      glVertex3fv( c );
      glColor3fv( cd );
      glVertex3fv( d );
      glColor3fv( ce );
      glVertex3fv( e );
      glColor3fv( cf );
      glVertex3fv( f );
      glColor3fv( cg );
      glVertex3fv( g );
      glColor3fv( ch );
      glVertex3fv( h );
      glEnd();
    }
  } else
    for ( int p=p0; p<=p1; p++ )
      draw( p, colour, 1. );
}


/** draw a tetrahedron
 *
 *  \param e      node to highlight
 *  \param colour colour to use
 *  \param size   size of point
 */
void Hexahedron :: draw( int t, GLfloat *colour, float size )
{
  glLineWidth( size );
  glColor4fv( colour );

  if ( t<_n ) {

    t *= 8;

    if (!_pt->vis(_node[t])||!_pt->vis(_node[t+1])||!_pt->vis(_node[t+2])||
        !_pt->vis(_node[t+3])||!_pt->vis(_node[t+4])||!_pt->vis(_node[t+5])||
        !_pt->vis(_node[t+6])||!_pt->vis(_node[t+7])  )
      return;

    const GLfloat* a =  _pt->pt(_node[t++]);
    const GLfloat* b =  _pt->pt(_node[t++]);
    const GLfloat* c =  _pt->pt(_node[t++]);
    const GLfloat* d =  _pt->pt(_node[t++]);
    const GLfloat* e =  _pt->pt(_node[t++]);
    const GLfloat* f =  _pt->pt(_node[t++]);
    const GLfloat* g =  _pt->pt(_node[t++]);
    const GLfloat* h =  _pt->pt(_node[t]);

    glBegin(GL_QUAD_STRIP);
    glVertex3fv( a );
    glVertex3fv( b );
    glVertex3fv( d );
    glVertex3fv( c );
    glVertex3fv( f );
    glVertex3fv( g );
    glVertex3fv( e );
    glVertex3fv( h );
    glVertex3fv( a );
    glVertex3fv( b );
    glEnd();
    glBegin(GL_QUADS);
    glVertex3fv( a );
    glVertex3fv( d );
    glVertex3fv( f );
    glVertex3fv( e );
    glVertex3fv( b );
    glVertex3fv( h );
    glVertex3fv( g );
    glVertex3fv( c );
    glEnd();

  }
}


/** read in the point file */
bool Hexahedron :: read( const char *fname )
{
  gzFile in;

  try {
    in = openFile( fname, "hex" );
  } catch (...) {return false;}

  const int bufsize=1024;
  char buff[bufsize];
  if ( gzgets(in, buff, bufsize) == Z_NULL ) return false;
  sscanf( buff, "%d", &_n );
  _node    = new int[8*_n];
  _region = new int[_n];

  for ( int i=0; i<_n; i++ ) {
    gzgets(in, buff, bufsize);
    if ( sscanf( buff, "%d %d %d %d %d %d %d %d %d", _node+8*i, _node+8*i+1,
                 _node+8*i+2, _node+8*i+3, _node+8*i+4, _node+8*i+5,
                 _node+8*i+6, _node+8*i+7, _region+i  ) < 9 ) {
      // if region not specified, assign a default of -1
      _region[i] = -1;
    }
  }
  gzclose(in);
}


/** draw the faces of an element not contaiining a node
 *
 * \param cn do not draw faces associated with this node
 */
void Hexahedron::draw_out_face( int cn )
{
  for ( int i=0; i<_n*_ptsPerObj; i++ )
    if ( _node[i] == cn ) {
      int nn      = i%_ptsPerObj;
      int elenode = i - nn;

      glBegin(GL_QUADS);
      if ( nn<4 ) {
        glVertex3fv( _pt->pt(_node[elenode+4]) );
        glVertex3fv( _pt->pt(_node[elenode+5]) );
        glVertex3fv( _pt->pt(_node[elenode+6]) );
        glVertex3fv( _pt->pt(_node[elenode+7]) );
      } else {
        glVertex3fv( _pt->pt(_node[elenode+0]) );
        glVertex3fv( _pt->pt(_node[elenode+1]) );
        glVertex3fv( _pt->pt(_node[elenode+2]) );
        glVertex3fv( _pt->pt(_node[elenode+3]) );
      }
      if ( nn==0 || nn==3 || nn==4 || nn==5 ) {
        glVertex3fv( _pt->pt(_node[elenode+1]) );
        glVertex3fv( _pt->pt(_node[elenode+7]) );
        glVertex3fv( _pt->pt(_node[elenode+6]) );
        glVertex3fv( _pt->pt(_node[elenode+2]) );
      } else {
        glVertex3fv( _pt->pt(_node[elenode+0]) );
        glVertex3fv( _pt->pt(_node[elenode+3]) );
        glVertex3fv( _pt->pt(_node[elenode+5]) );
        glVertex3fv( _pt->pt(_node[elenode+4]) );
      }
      if ( nn==0 || nn==1 || nn==7 || nn==4 ) {
        glVertex3fv( _pt->pt(_node[elenode+3]) );
        glVertex3fv( _pt->pt(_node[elenode+2]) );
        glVertex3fv( _pt->pt(_node[elenode+6]) );
        glVertex3fv( _pt->pt(_node[elenode+5]) );
      } else {
        glVertex3fv( _pt->pt(_node[elenode+0]) );
        glVertex3fv( _pt->pt(_node[elenode+4]) );
        glVertex3fv( _pt->pt(_node[elenode+7]) );
        glVertex3fv( _pt->pt(_node[elenode+1]) );
      }
      glEnd();
    }
}


/** determine if a clipping plane intersects an element.
 *
 *  \param pd     visible points
 *  \param cp     clipping plane, cp[0]x + cp[1]y +cp[2]z + cp[3] = 0
 *  \param e      the element in the list
 *  \param interp construct to interpolate data
 *
 *  \return a surface element if intersection, NULL otherwise
 *
 *  \post interp may be allocated
 */
SurfaceElement*
Hexahedron::cut( char*pd, GLfloat* cp,
                 Interpolator<DATA_TYPE>* &interp, int e )
{
  return planecut( pd, cp, interp, hex_num_edge, hex_edges, e );
}


